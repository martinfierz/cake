	int reverse(int x) {
		// return a reverted 8-bit pattern, i.e. if you have pattern
		// e.g. 00001111 return 11110000, or if you have 11000000 return 00000011
		int index = 0;
		int i; 
		for (i = 0; i < 8; i++) {
			if (x & (1 << i))
				index += (1 << (7 - i)); 
		}
		return index; 
	}

	int reverse12(int x) {
		// return a reverted 12-bit pattern, i.e. if you have pattern
		// e.g. 00001111 return 11110000, or if you have 11000000 return 00000011
		int index = 0;
		int i;
		for (i = 0; i < 12; i++) {
			if (x & (1 << i))
				index += (1 << (11 - i));
		}
		return index;
	}

